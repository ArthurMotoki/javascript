// Estrutura básica do "WHILE"
var c = 1
while (c < 6) { 
    console.log('Tudo bem?')
    c++ // c = c + 1
}

//----------------------------------------------------------------------------------------------------------------------------

var c = 1 
do {
    console.log('Tudo bem?')
    c++
} while (c < 10)


//Existem duas formas de estrutura de repetição, "while" e "do while". As duas podem ser usadas em momentos diferentes, mas são basicamente as mesma coisa.